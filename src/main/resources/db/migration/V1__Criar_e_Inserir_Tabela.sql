CREATE TABLE ENDERECO
(
    ID VARCHAR(36) NOT NULL,
    LOGRADOURO VARCHAR(80) NOT NULL,
    NUMERO_RESIDENCIAL VARCHAR(10) NOT NULL,
    COMPLEMENTO VARCHAR(50) NOT NULL,
    BAIRRO VARCHAR(50) NOT NULL,
    CIDADE VARCHAR(50) NOT NULL,
    CEP VARCHAR(8) NOT NULL,
    UF VARCHAR(2) NOT NULL,

    PRIMARY KEY (ID)
);

CREATE TABLE USUARIO
(
    ID   VARCHAR(36) NOT NULL,
    IDENTIFICACAO VARCHAR(10) NOT NULL,
    NOME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(50) NOT NULL,
    USUARIO VARCHAR(50) NOT NULL,
    SENHA VARCHAR(8) NOT NULL,
    ENDERECO_FK VARCHAR(36),

    PRIMARY KEY(ID),
    FOREIGN KEY (ENDERECO_FK) REFERENCES ENDERECO (ID)
);

CREATE TABLE USUARIO_ROLE (
    USUARIO_ID VARCHAR(36) NOT NULL,
    ROLE_USUARIO VARCHAR(7) NOT NULL,
    PRIMARY KEY (USUARIO_ID, ROLE_USUARIO),
    FOREIGN KEY (USUARIO_ID) REFERENCES USUARIO(ID),
    CHECK (ROLE_USUARIO IN ('DEFAULT', 'USER', 'EMPRESA', 'ADMIN'))
);

CREATE TABLE EMPRESA
(
    ID VARCHAR(36) NOT NULL,
    NOME_FANTASIA VARCHAR(100) NOT NULL,
    RAZAO_SOCIAL VARCHAR(100) NOT NULL,
    CNPJ VARCHAR(14) NOT NULL,
    EMAIL VARCHAR(50) NOT NULL,
    TELEFONE VARCHAR(13) NOT NULL,
    ENDERECO_FK VARCHAR(36),

    PRIMARY KEY(ID),
    FOREIGN KEY (ENDERECO_FK) REFERENCES ENDERECO (ID)
);

