    PRIMARY KEY (ID),
    FOREIGN KEY (BANCO_FK) REFERENCES BANCO (ID),
    FOREIGN KEY (USUARIO_FK) REFERENCES USUARIO (ID),
    FOREIGN KEY (EMPRESA_FK) REFERENCES EMPRESA (ID)
